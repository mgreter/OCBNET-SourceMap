package OCBNET::SourceMap::Map;
####################################################################################################

use utf8;
use strict;
use warnings;

####################################################################################################

sub new
{

	# get package name
	my ($pkg) = @_;

	# create new hash reference
	# initialize the rows array
	my $self = { 'rows' => [] };

	# return blessed object
	return bless $self, $pkg;

}

####################################################################################################
####################################################################################################
1;